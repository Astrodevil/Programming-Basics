# text = str
# numeric = int, float
# sequence = list, tuple, range
# mapping = dict
# boolean = bool

# str
x = "Hello World"
print(x)

# int 
x = 12
print(x)

# float
x = 21.2
print(x)

# list
x = ["apple", "banana", "watermelon"]
print(x)

# tuple
x = ("apple", "banana", "watermelon")
print(x)

# range
x = range(4)
print(x)

# dict
x = {"name": "alif", "age": 22}
print(x)

# boolean
x = True
print(x)
y = False
print(y)